// Problem 1
// Find and print the total sum of all the numbers in the list.
let numbers = [7, 6, 5, 4, 3, 2, 1];

let sum = 0;
for (let i = 0 ; i < numbers.length; i++) { 
    let item = numbers[i]; 
    sum = sum + item; 
}
console.log("The sum is:", sum);


